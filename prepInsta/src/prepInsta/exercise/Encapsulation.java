package prepInsta.exercise;

public class Encapsulation {
	private int num;
	private String name ;
	private int age;
	
	
	
	public void setnum(int num)
	{
		this.num=num;
	}
	public int getnum()
	{
		return num;
	}
	public void setage(int age)
	{
		this.age=age;
	}
	public int getage()
	{
		return age;
	}
	public void setname(String name )
	{
		this.name=name;
	}
	public String getname()
	{
		return name ;
	}
	

}
